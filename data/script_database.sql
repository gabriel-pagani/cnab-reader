DROP TABLE IF EXISTS CONCILIACAO_BANCARIA;

CREATE TABLE CONCILIACAO_BANCARIA (
    ID INT IDENTITY(1,1) PRIMARY KEY,        
    CNPJ VARCHAR(18),
    BANCO VARCHAR(50),
    CONTA VARCHAR(50),
    AGENCIA VARCHAR(50),
    CONVENIO VARCHAR(50),
    DATALAN DATE,
    VALORLAN VARCHAR(50),
    TIPOLAN VARCHAR(50),
    DESCLAN VARCHAR(50),
);
