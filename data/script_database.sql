DROP TABLE IF EXISTS TCNAB;

CREATE TABLE TCNAB (
    ID INT IDENTITY(1,1) PRIMARY KEY,        
    COLIGADA VARCHAR(18),
    BANCO VARCHAR(3),
    CONVENIO VARCHAR(20),
    AGENCIA VARCHAR(5),
    CONTA VARCHAR(12),
    DATALAN DATE,
    VALORLAN DECIMAL(16,2),
    TIPOLAN VARCHAR(1),
    DESCLAN VARCHAR(25),
);
