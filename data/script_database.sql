DROP TABLE IF EXISTS ZCNAB;

CREATE TABLE ZCNAB (
    ID INT IDENTITY(1,1) PRIMARY KEY,        
    COLCNPJ VARCHAR(18),
    BANCO VARCHAR(3),
    CONVENIO VARCHAR(20),
    AGENCIA VARCHAR(5),
    CONTA VARCHAR(12),
    DATALAN DATE,
    VALORLAN DECIMAL(16,2),
    TIPOLAN CHAR(1) CHECK (TIPOLAN IN ('C', 'D')),
    DESCLAN VARCHAR(25),
    TIPOREGIS CHAR(1) CHECK (TIPOREGIS IN ('1', '3', '5')),
    DATA_IMPORTACAO DATETIME DEFAULT GETDATE()
);

CREATE INDEX IDX_CNPJ ON ZCNAB(COLCNPJ);
CREATE INDEX IDX_DATALAN ON ZCNAB(DATALAN);
CREATE INDEX IDX_BANCO ON ZCNAB(BANCO);